                        <h1>Who is KAS?</h1>
                        
                        <p>KAS was created by the brand’s designer and founder, Kirat S. Anand. Born and raised in New York with no formal training, Kirat knew that he wanted to make an imprint on the fashion world. He began his career as an investment banker at JPMorgan, but it wasn’t long before he left to follow his heart and become a designer. Growing up in the industry, where his father was a self-made manufacturer and mother a designer, he didn't need to look far for inspiration or support. From an early age, Kirat would visit India months at a time and spend countless hours at the factories and sampling rooms. He was surrounded by artisans, pattern makers and tailors, making his transition into fashion almost inevitable. After a few years of learning the craft of tailoring, attending to details and understanding key components of the production process, he set off to embark on his own endeavor.</p>
                        
                        <p>Realizing a void in the affordable luxury/contemporary woman's ready-to-wear market, he launched KAS New York in 2007. The disparate influences of his life experiences and cultural heritage formed an aesthetic that combines neotraditional urban style with luxurious bohemian vintage accents to create urbane lux sophistication.  "My desire was to create a lifestyle brand, which wasn't ageist or restrictive. There were so many options for the younger girl, but a modern sophisticated woman either had to go couture, missy or moderate. My goal was to create a brand for women who embrace their style and dress with the 3 Fs in mind; having Fun, being Fashionable and not spending a Fortune."</p>
                        
                        <p>With striking silhouettes, a vibrant color palette and inventive patterns which transition from day to night, KAS’ versatile pieces have become must-have staples in women's closets. In 2008, Kirat was nominated by The International Design Awards for the Fashion Designer of the Year award in the Prêt-a-Porter category. In 2009, he created a capsule kids collection, KAS KIDTURE, for Saks and Malia Obama was wearing his KAS outfit at the Inaugural Concert. In addition to celebrity following, KAS has been featured in the pages of Woman's Wear Daily, Glamour, People, Lucky, InStyle, InTouch, US Weekly, Seventeen, Health, Latina, Audrey, New York Newsday and many other magazines worldwide. The collection is available in high-end boutiques and exclusive department stores.</p>
                        
                        
